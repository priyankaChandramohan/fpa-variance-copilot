from variance_engine import get_material_items


# ── Slide dimensions ──────────────────────────────────────────────────────────

_W  = Inches(13.33)
_H  = Inches(7.5)

# Content area inside all slides (except title)
_ML = 0.55   # left margin, inches
_MR = 0.55   # right margin, inches
_CW = 13.33 - _ML - _MR   # content width: 12.23"
_CT = 1.10   # content top (below slide title area)
_CH = 7.5 - _CT - 0.25    # content height: 6.15"


# ── Color palette ─────────────────────────────────────────────────────────────

