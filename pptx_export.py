def _clean_commentary(text: str) -> list[tuple[str, bool]]:
    """
    Strip markdown formatting and return a list of (paragraph_text, is_bold) tuples.

    Rules applied
    -------------
    - ``**text**`` and ``*text*`` → plain text
    - ``# heading`` → plain text  (marked bold if starts with # or is ALL CAPS)
    - ``---`` separator lines → skipped
    - Blank lines → skipped (paragraph spacing is handled by space_before on headers)
    - Lines that are ALL CAPS or start with ``#`` → rendered bold as section headers
    """
    text   = re.sub(r"\*\*(.+?)\*\*", r"\1", text)
    text   = re.sub(r"\*(.+?)\*",     r"\1", text)
    text   = re.sub(r"^#{1,4}\s+",    "",    text, flags=re.MULTILINE)

    result = []
    for line in text.splitlines():
        line = line.rstrip()
        if not line or re.match(r"^-{3,}$", line):
            continue
        # Section header heuristic: all-caps word run ≥ 3 chars, or starts capital-colon
        stripped = line.strip()
        is_header = bool(
            re.match(r"^[A-Z][A-Z\s&/·\-:]{2,}$", stripped)      # ALL CAPS line
            or re.match(r"^[A-Z][A-Za-z\s]+:\s*$", stripped)      # "Title:" pattern
        )
        result.append((stripped, is_header))

    return result


def _clean_text(text: str) -> str:
    """Strip markdown for single-line text boxes (narrative, subtitle)."""
    text = re.sub(r"\*\*(.+?)\*\*", r"\1", text)
    text = re.sub(r"\*(.+?)\*",     r"\1", text)
    text = re.sub(r"^#{1,4}\s+",    "",    text, flags=re.MULTILINE)
    return text.strip()


def _rgb(hex_color: str) -> RGBColor:
    """Convert a 6-char CSS hex string to a python-pptx RGBColor."""
    h = hex_color.lstrip("#")
    return RGBColor(int(h[0:2], 16), int(h[2:4], 16), int(h[4:6], 16))


def _d(value: float, compact: bool = False, sign: bool = False) -> str:
    """Format *value* as a currency string."""
    if pd.isna(value):
        return "—"
    prefix = ("+" if value >= 0 else "") if sign else ""
    neg    = "-" if value < 0 else ""
    abs_v  = abs(value)
    if compact:
        if abs_v >= 1_000_000:
            return f"{prefix}{neg}${abs_v / 1_000_000:.1f}M"
        if abs_v >= 1_000:
            return f"{prefix}{neg}${abs_v / 1_000:.0f}K"
    return f"{prefix}${value:,.0f}"


def _p(value: float) -> str:
    """Format *value* as a signed percentage string."""
    if pd.isna(value):
        return "—"
    return f"{value:+.1f}%"
