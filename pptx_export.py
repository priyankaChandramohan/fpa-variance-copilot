"""
pptx_export.py — Management-grade PowerPoint export for FP&A Variance Copilot.

Produces a 6-slide management pack that looks like it was built by an FP&A team,
not generated by a script.  Every layout decision — spacing, font sizes, color
allocation — was made with a real presentation context in mind.

Slide structure
---------------
1. Title          — dark slate background, period + tone label, timestamp
2. Executive KPIs — 4 teal-accented card boxes on light background
3. Waterfall      — Plotly chart rendered to PNG, embedded full-width
4. Variances      — material items table, header in dark slate, alt row shading
5. Commentary     — cleaned text with left teal accent bar, tone in title
6. Forward Look   — projection table + risk narrative

Dependencies
------------
python-pptx  (layout + text)
kaleido      (chart → PNG — graceful fallback if missing)
"""

from __future__ import annotations

